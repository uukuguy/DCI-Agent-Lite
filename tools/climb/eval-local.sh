#!/usr/bin/env bash
set -euo pipefail

if [ "$#" -ne 1 ]; then
    echo "Usage: $0 RUN_DIR" >&2
    exit 2
fi

RUN_DIR="$1"
mkdir -p "$RUN_DIR"
PYTHON_BIN="${PYTHON:-}"
HYPOTHESIS_ID="${DCI_CLIMB_HYPOTHESIS_ID:-H-001}"

run_python() {
    if [ -n "$PYTHON_BIN" ]; then
        "$PYTHON_BIN" "$@"
    else
        uv run python "$@"
    fi
}

run_dimension() {
    name="$1"
    test_name="$2"
    module_name="${test_name#tests.}"
    module_name="${module_name%%.*}"
    if [ ! -f "tests/$module_name.py" ] && [ -f "asterion/tests/$module_name.py" ]; then
        command=(run_python -m unittest "$test_name" -v)
        test_cwd="asterion"
    else
        command=(run_python -m unittest "$test_name" -v)
        test_cwd="."
    fi
    if (cd "$test_cwd" && "${command[@]}") >"$RUN_DIR/$name.log" 2>&1; then
        printf '1'
    else
        printf '0'
    fi
}

run_rust_dimension() {
    name="$1"
    test_name="$2"
    if cargo test \
        --manifest-path asterion/packages/rust/controlled-executor/Cargo.toml \
        --test "$rust_suite" \
        "$test_name" -- --exact >"$RUN_DIR/$name.log" 2>&1; then
        printf '1'
    else
        printf '0'
    fi
}

run_closure_dimension() {
    name="$1"
    check="$2"
    case "$check" in
        operator_binary)
            command=(cargo test --manifest-path asterion/packages/rust/controlled-executor/Cargo.toml --test operator)
            ;;
        operator_docs)
            command=(uv run python -m unittest tests.test_climb_tools.ClimbToolTests.test_af050_operator_docs_and_root_verification_targets_exist -v)
            ;;
        root_test_target)
            command=(make test-rust-executor)
            ;;
        root_check_target)
            command=(make check-rust-executor)
            ;;
        assembly_typescript)
            command=(npm --prefix asterion/packages/typescript/asterion-runtime test)
            ;;
        assembly_docs)
            command=(uv run python -m unittest tests.test_application_assembly.AssemblyDocumentationTests -v)
            ;;
        assembly_non_execution)
            command=(node --test --test-name-pattern "keeps assembly resolution outside" asterion/packages/typescript/asterion-runtime/test/runtime.test.mjs)
            ;;
        assembly_closure)
            command=(uv run python -m unittest tests.test_climb_tools.ClimbToolTests.test_af090_h004_train_runs_full_framework_closure_gate -v)
            ;;
        asterion_closure)
            command=(uv run python -m unittest tests.test_climb_tools.ClimbToolTests.test_af095_h004_train_runs_full_framework_closure_gate -v)
            ;;
        asterion_cli)
            command=(uv run python -m unittest tests.test_distribution_boundaries.SourceDistributionBoundaryTests.test_source_baseline_remains_runnable_without_installation -v)
            ;;
        asterion_examples)
            command=(uv run python -m unittest tests.test_asterion_structure.AsterionStructureTests.test_examples_execute_with_pairwise_semantic_parity -v)
            ;;
        asterion_architecture)
            command=(uv run python -m unittest tests.test_asterion_structure.AsterionStructureTests.test_layout_guide_defines_framework_ownership -v)
            ;;
        runner_docs)
            command=(uv run python -m unittest tests.test_application_runner.ApplicationRunnerDocumentationTests.test_guide_documents_the_explicit_plan_runtime_and_service_boundary tests.test_application_runner.ApplicationRunnerDocumentationTests.test_guide_documents_immutable_results_and_safe_failures -v)
            ;;
        runner_boundary)
            command=(uv run python -m unittest tests.test_application_runner.ApplicationRunnerDocumentationTests.test_runner_boundary_excludes_control_plane_and_process_ownership -v)
            ;;
        runner_language)
            command=(uv run python -m unittest tests.test_application_runner.ApplicationRunnerDocumentationTests.test_runner_is_python_owned_without_dci_or_typescript_duplicate -v)
            ;;
        runner_closure)
            command=(uv run python -m unittest tests.test_climb_tools.ClimbToolTests.test_af100_h004_train_runs_full_framework_closure_gate -v)
            ;;
        application_python)
            command=(bash -c 'modules=(); for path in tests/test_*.py; do [ "${path##*/}" = "test_climb_tools.py" ] && continue; modules+=("${path%.py}"); done; uv run python -m unittest "${modules[@]//\//.}" -v && (cd asterion && uv run python -m unittest discover -s tests -v) && uv run python -m unittest tests.test_climb_tools.ClimbToolTests.test_af210_train_registers_every_application_parity_hypothesis -v')
            ;;
        application_quality)
            command=(bash -c 'uv run python -m compileall -q src asterion/src/asterion asterion/tests tests tools && uv run ruff check src asterion/src/asterion asterion/tests tests tools && bash -n tools/climb/train.sh tools/climb/eval-local.sh tools/climb/cycle.sh')
            ;;
        application_typescript)
            command=(npm --prefix asterion/packages/typescript/asterion-runtime test)
            ;;
        application_system)
            command=(bash -c 'make test-rust-executor && make check-rust-executor && python3 tools/project_scope_check.py && git diff --check')
            ;;
        *)
            echo "ERROR: unknown closure check $check" >&2
            return 2
            ;;
    esac
    if "${command[@]}" >"$RUN_DIR/$name.log" 2>&1; then
        printf '1'
    else
        printf '0'
    fi
}

run_typescript_dimension() {
    name="$1"
    test_name="$2"
    if [ "$test_name" = "public_type_contract" ]; then
        command=(npm --prefix asterion/packages/typescript/asterion-runtime run build)
    else
        command=(
            node --test --test-name-pattern "$test_name"
            asterion/packages/typescript/asterion-runtime/test/runtime.test.mjs
        )
    fi
    if npm --prefix asterion/packages/typescript/asterion-runtime run build >"$RUN_DIR/$name.log" 2>&1 \
        && "${command[@]}" >>"$RUN_DIR/$name.log" 2>&1; then
        printf '1'
    else
        printf '0'
    fi
}

